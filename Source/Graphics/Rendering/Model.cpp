#include "Model.h"

namespace Derpy { namespace Graphics {

		Model::Model(float x, float y, float z, float width, float height, float depth, unsigned int color)
			: Renderable3D(Maths::Vec3f(x, y, z), Maths::Vec3f(width, height, depth), color), position(m_Position){

		}

		Model::Model(float x, float y, float z, float width, float height, float depth, Texture* texture)
			: Renderable3D(Maths::Vec3f(x, y, z), Maths::Vec3f(width, height, depth), 0xffffffff), position(m_Position){
			m_Texture = texture;
		}

} }