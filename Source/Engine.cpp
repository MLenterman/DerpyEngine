#include "Engine.h"

namespace Derpy {

	System::IGraphicsDriver* Engine::getGraphicsDriver() const{
		return m_GraphicsDriver;
	}

	bool Engine::setGraphicsDriver(System::EGraphicsDriver driver){
		if (m_GraphicsDriver != NULL)
			delete m_GraphicsDriver;

		switch (driver){
		case System::EGraphicsDriver::OPENGL:
			m_GraphicsDriver = new System::GraphicsDriver_GL(driver);
			return true;
		default:
			return false;
		}

	}


}